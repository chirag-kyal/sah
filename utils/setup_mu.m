function [ mu ] = setup_mu( n )
%%%%%%%%%%%%% Setup mu %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
% mu = [0 0 0 0 .0005 .0005 .0005 .0005 .001 .001...
%       .001 .002 .002 .005 .005 .01 .01 .05 .05 .2 .2 .5 .5 .7 .7 1 1];

mu = zeros(n, 1);
mu(1) = 10^-5;

for i = 2:n
    mu(i) = mu(i-1) * 2;
end
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


end

